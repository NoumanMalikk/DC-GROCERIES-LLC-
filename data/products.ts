function placeholderImage(title: string): ProductImage {
  return {
    src: "/products/placeholder.svg",
    alt: `Exact product image required for ${title}`,
    type: "placeholder",
    width: 800,
    height: 800,
  };
}

function baseProduct(
  partial: Omit<
    Product,
    | "currency"
    | "ingredients"
    | "nutritionFacts"
    | "allergenInformation"
    | "storageInstructions"
    | "preparationInstructions"
    | "countryOfOrigin"
    | "manufacturer"
    | "distributor"
    | "barcode"
    | "dimensions"
    | "imageGallery"
    | "imageSourceRecord"
    | "imageVerificationStatus"
    | "labelVerificationStatus"
    | "productionReady"
    | "supplierSku"
    | "unitPrice"
    | "brand"
  > &
    Partial<
      Pick<
        Product,
        | "ingredients"
        | "allergenInformation"
        | "storageInstructions"
        | "preparationInstructions"
        | "countryOfOrigin"
        | "manufacturer"
        | "distributor"
        | "barcode"
        | "supplierSku"
        | "unitPrice"
        | "brand"
        | "dimensions"
        | "weight"
      >
    >
): Product {
  const {
    brand,
    supplierSku,
    unitPrice,
    ingredients,
    allergenInformation,
    storageInstructions,
    preparationInstructions,
    countryOfOrigin,
    manufacturer,
    distributor,
    barcode,
    dimensions,
    weight,
    ...rest
  } = partial;

  return {
    currency: "USD",
    nutritionFacts: pendingNutrition,
    imageGallery: [placeholderImage(partial.title)],
    imageSourceRecord: `img-${partial.id}`,
    imageVerificationStatus: "missing",
    labelVerificationStatus: "pending",
    productionReady: false,
    brand: brand ?? null,
    supplierSku: supplierSku ?? null,
    unitPrice: unitPrice ?? null,
    ingredients: ingredients ?? null,
    allergenInformation: allergenInformation ?? null,
    storageInstructions: storageInstructions ?? null,
    preparationInstructions: preparationInstructions ?? null,
    countryOfOrigin: countryOfOrigin ?? "Verification required",
    manufacturer: manufacturer ?? "Verification required",
    distributor: distributor ?? "Pending supplier documentation",
    barcode: barcode ?? null,
    dimensions: dimensions ?? {
      lengthIn: null,
      widthIn: null,
      heightIn: null,
    },
    weight: weight ?? null,
    ...rest,
  };
}

