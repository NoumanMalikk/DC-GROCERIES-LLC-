export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getProductBySku(sku: string): Product | undefined {
  return products.find((p) => p.sku === sku);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.featured);
}

export function getProductsByCollection(collectionSlug: string): Product[] {
  return products.filter((p) => p.collectionSlugs.includes(collectionSlug));
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter(
    (p) =>
      p.category.toLowerCase() === category.toLowerCase() ||
      p.subcategory.toLowerCase() === category.toLowerCase()
  );
}

export function getRelatedProducts(product: Product): Product[] {
  return product.relatedProductIds
    .map((id) => getProductById(id))
    .filter((p): p is Product => Boolean(p));
}

export function getCrossSellProducts(product: Product): Product[] {
  return product.crossSellProductIds
    .map((id) => getProductById(id))
    .filter((p): p is Product => Boolean(p));
}
